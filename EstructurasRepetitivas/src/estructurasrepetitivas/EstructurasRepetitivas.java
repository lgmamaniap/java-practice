package estructurasrepetitivas;

public class EstructurasRepetitivas {

    public static void main(String[] args) {
        // do while loop
        int counter = 0;
        do{
            System.out.println(counter);
            counter++;
        }
        while(counter < 10);
        
        // For loop
        int forCounter = 10;
        for (int i = 0; i < forCounter; i++) {
            System.out.println(i);
        }
    }
    
}
